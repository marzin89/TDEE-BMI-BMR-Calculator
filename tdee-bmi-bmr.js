function updateToImperial() {

    const weight = document.getElementById('weight');

    const height = document.getElementById('height');

    weight.value = 'lbs';
        
    height.value = 'ft\' in"';

}

function updateToMetric() {

    const weight = document.getElementById('weight');

    const height = document.getElementById('height');

    weight.value = 'kg';

    height.value = 'cm';

}



